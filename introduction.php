			<h1>Premisse</h1>
		</div>
		<p>A family saga with a supernatural twist, set in a German town, where the disappearance of two young children exposes the relationships among four families.</p>
		<p>Set in the fictional town of Winden, Germany, Dark concerns the aftermath of a child's disappearance which exposes the secrets of, and hidden connections among, four estranged families as they slowly unravel a sinister time travel conspiracy which spans three generations.</p>
		<p>The first season primarily takes place in 2019 but expands to include stories set in 1986, 1953, and 2052, with several characters being portrayed at various ages by multiple actors.</p>
		<p>The second season takes place several months after the first, depicting the initial stories in 2020, 1987, and 1954, respectively, while continuing the future-set story-line into 2053, and adding a fifth story-line, set in 1921.</p>
		<p>Throughout the series, Dark explores the existential implications of time and its effects upon human nature.</p>
